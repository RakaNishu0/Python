print("Hello 'World'")
print('Hello "World"')

print('Hello'+'World'+'!')                  ## 문자열은 더해서 붙일 수 있다

print('Hello '*3)                           ## 문자열은 곱해서 반복할 수 있다
                                            ## 빼기 / 나누기는 사용할 수 없다

print('Hello'[1])

print(10+5)                                 ## 숫자데이터는 산술연산 적용
print("10"+"5")                             ## 문자데이터는 붙여쓰기 적용됨 - 데이터 타입에 따라 같은 기호의 수행 방식이 달라진다
