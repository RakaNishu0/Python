print('hello'.capitalize())                             ## 첫 글자를 대문자로
print('hello world'.upper())                            ## 전체를 대문자로

print('hello world'.__len__())                          ## 문자열의 길이를 확인
print(len('hello world'))                               ## 또 다른 방법

print("hello world".replace('world','programming'))     ## 문자열 치환
