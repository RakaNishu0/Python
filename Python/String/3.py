print("rakanishu\'s \"python novice\"")                                 ## '\'(escape) 기호는 해당 기호가 역할을 수행하지 않는 단순 문자임을 표시해주는 특수기호
print("rakanishu\'s \"python novice\"".capitalize())
print("rakanishu\'s \"python novice\"".replace('novice','master'))

print("\\")             ## 앞의 \가 뒤에 있는 \ 의 역할(escape)을 하지 않는 단순 문자임을 표시해 줌

print("Hello \nworld")              ## \n = \ 가 n을 문자로 만드는 것이 아니라, n과 합쳐져 new line 기능을 수행
print("Hello\t\tworld")             ## \t = tap 을 수행, 2번 넣으면 2번 적용
print("\a")                         ## \a = 경고(alert)음을 내주는 기능
